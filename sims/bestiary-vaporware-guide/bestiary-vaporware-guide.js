let currentTech = 0;
let selectedBeast = -1;
let selectedVapor = -1;
let submitted = false;
let scores = [];
let beastSelect;
let submitBtn, nextBtn, resetBtn;

function updateCanvasSize() {
  const container = document.querySelector('main');
  if (container) {
    canvasWidth = container.offsetWidth;
  }
  canvasHeight = drawHeight + controlHeight;
}

function setup() {
  updateCanvasSize();
  const canvas = createCanvas(canvasWidth, canvasHeight);
  canvas.parent(document.querySelector('main'));
  textFont('Arial');

  scores = new Array(technologies.length).fill(-1); // -1=unanswered, 0=wrong, 1=partial, 2=both correct

  // Beast dropdown
  beastSelect = createSelect();
  beastSelect.parent(document.querySelector('main'));
  beastSelect.option('-- Select a Beast --', '-1');
  for (let i = 0; i < beasts.length; i++) {
    beastSelect.option(beasts[i].name + " - " + beasts[i].desc, String(i));
  }
  beastSelect.changed(() => {
    selectedBeast = parseInt(beastSelect.value());
  });

  submitBtn = createButton('Submit Classification');
  submitBtn.parent(document.querySelector('main'));
  submitBtn.mousePressed(doSubmit);

  nextBtn = createButton('Next Technology');
  nextBtn.parent(document.querySelector('main'));
  nextBtn.mousePressed(doNext);

  resetBtn = createButton('Reset');
  resetBtn.parent(document.querySelector('main'));
  resetBtn.mousePressed(doReset);
}

function draw() {
  updateCanvasSize();
  resizeCanvas(canvasWidth, canvasHeight);

  // Background
  fill('aliceblue');
  stroke('silver');
  rect(0, 0, canvasWidth, drawHeight);
  fill('white');
  noStroke();
  rect(0, drawHeight, canvasWidth, controlHeight);

  let tech = technologies[currentTech];
  let margin = 20;
  let cardLeft = margin;
  let cardTop = 10;
  let cardW = canvasWidth - margin * 2;

  // Title
  fill(30);
  noStroke();
  textSize(16);
  textAlign(CENTER, TOP);
  text("Bestiary Vaporware Guide", canvasWidth / 2, cardTop);

  // Progress dots
  let dotY = cardTop + 24;
  let dotSpacing = 18;
  let dotsStartX = canvasWidth / 2 - (technologies.length * dotSpacing) / 2;
  for (let i = 0; i < technologies.length; i++) {
    let dx = dotsStartX + i * dotSpacing + dotSpacing / 2;
    if (scores[i] === 2) {
      fill(76, 175, 80);
    } else if (scores[i] === 1) {
      fill(255, 200, 0);
    } else if (scores[i] === 0) {
      fill(220, 60, 60);
    } else {
      fill(200);
    }
    noStroke();
    if (i === currentTech) {
      stroke(30);
      strokeWeight(2);
    }
    ellipse(dx, dotY, 12, 12);
    noStroke();
  }

  // Technology card
  let techCardTop = dotY + 16;
  stroke(100);
  strokeWeight(1);
  fill(255);
  rect(cardLeft, techCardTop, cardW, 130, 8);

  fill(30);
  noStroke();
  textAlign(LEFT, TOP);
  textSize(15);
  textStyle(BOLD);
  text(tech.name, cardLeft + 12, techCardTop + 10);
  textStyle(NORMAL);
  textSize(12);
  fill(60);
  text(tech.description, cardLeft + 12, techCardTop + 30, cardW - 24);
  textSize(11);
  fill(100);
  textStyle(ITALIC);
  text("Claims: " + tech.claims, cardLeft + 12, techCardTop + 60, cardW - 24);
  textStyle(NORMAL);
  fill(60);
  text("Status: " + tech.status, cardLeft + 12, techCardTop + 92, cardW - 24);

  // Vaporware selector rectangles
  let vaporY = techCardTop + 142;
  noStroke();
  fill(30);
  textSize(12);
  textAlign(LEFT, TOP);
  text("Vaporware Level:", cardLeft, vaporY + 6);

  let vaporStartX = cardLeft + 120;
  let vaporBtnW = (cardW - 130) / 5 - 4;
  let vaporBtnH = 28;

  for (let i = 0; i < 5; i++) {
    let bx = vaporStartX + i * (vaporBtnW + 4);
    let c = vaporColors[i];

    if (selectedVapor === i) {
      stroke(30);
      strokeWeight(3);
    } else {
      stroke(150);
      strokeWeight(1);
    }
    fill(c[0], c[1], c[2], selectedVapor === i ? 255 : 160);
    rect(bx, vaporY, vaporBtnW, vaporBtnH, 4);

    fill(i >= 3 && selectedVapor !== i ? 60 : 30);
    noStroke();
    textSize(10);
    textAlign(CENTER, CENTER);
    text(vaporLevels[i], bx + vaporBtnW / 2, vaporY + vaporBtnH / 2);
  }

  // Beast dropdown label
  let beastLabelY = vaporY + vaporBtnH + 12;
  fill(30);
  noStroke();
  textSize(12);
  textAlign(LEFT, TOP);
  text("Beast Classification:", cardLeft, beastLabelY + 4);

  // Position beast dropdown
  beastSelect.position(cardLeft + 140, beastLabelY);
  beastSelect.size(min(cardW - 160, 420));

  // Analysis card (after submit)
  if (submitted) {
    let analysisTop = beastLabelY + 36;
    let beastCorrect = selectedBeast >= 0 && beasts[selectedBeast].name === tech.correctBeast;
    let vaporCorrect = selectedVapor === tech.correctVapor;

    // Results header
    let resultColor;
    let resultText;
    if (beastCorrect && vaporCorrect) {
      resultColor = [76, 175, 80];
      resultText = "Both correct.";
    } else if (beastCorrect || vaporCorrect) {
      resultColor = [255, 180, 0];
      resultText = "Partially correct.";
    } else {
      resultColor = [220, 60, 60];
      resultText = "Both incorrect.";
    }

    stroke(resultColor[0], resultColor[1], resultColor[2]);
    strokeWeight(2);
    fill(255, 252, 245);
    rect(cardLeft, analysisTop, cardW, 140, 8);

    fill(resultColor[0], resultColor[1], resultColor[2]);
    noStroke();
    textSize(13);
    textAlign(LEFT, TOP);
    textStyle(BOLD);
    text(resultText, cardLeft + 12, analysisTop + 8);
    textStyle(NORMAL);

    fill(60);
    textSize(11);
    text("Correct beast: " + tech.correctBeast + "  |  Correct level: " + vaporLevels[tech.correctVapor], cardLeft + 12, analysisTop + 26);

    fill(40);
    textSize(11);
    text(tech.analysis, cardLeft + 12, analysisTop + 44, cardW - 24);

    // Confidence bar
    let confY = analysisTop + 118;
    fill(80);
    textSize(11);
    textAlign(LEFT, CENTER);
    text("Viability in 5 years: " + tech.confidence + "%", cardLeft + 12, confY);

    let barX = cardLeft + 170;
    let barW = cardW - 190;
    stroke(150);
    strokeWeight(1);
    fill(220);
    rect(barX, confY - 6, barW, 12, 3);
    noStroke();
    let confW = barW * tech.confidence / 100;
    if (tech.confidence > 50) {
      fill(76, 175, 80);
    } else if (tech.confidence > 25) {
      fill(255, 180, 0);
    } else {
      fill(220, 60, 60);
    }
    rect(barX, confY - 6, confW, 12, 3);
  }

  // Score summary
  let totalCorrect = scores.filter(s => s === 2).length;
  let totalPartial = scores.filter(s => s === 1).length;
  let totalAnswered = scores.filter(s => s >= 0).length;

  fill(30);
  noStroke();
  textSize(12);
  textAlign(CENTER, TOP);
  text("Score: " + totalCorrect + " perfect, " + totalPartial + " partial out of " + totalAnswered + " answered", canvasWidth / 2, drawHeight - 20);

  // Position buttons
  let btnY = drawHeight + 15;
  let btnSpacing = 140;
  let btnStartX = canvasWidth / 2 - btnSpacing * 1.2;
  submitBtn.position(btnStartX, btnY);
  nextBtn.position(btnStartX + btnSpacing, btnY);
  resetBtn.position(btnStartX + btnSpacing * 2, btnY);
}

function mousePressed() {
  if (submitted) return;

  let tech = technologies[currentTech];
  let margin = 20;
  let cardLeft = margin;
  let cardW = canvasWidth - margin * 2;
  let techCardTop = 10 + 24 + 16;
  let vaporY = techCardTop + 142;
  let vaporStartX = cardLeft + 120;
  let vaporBtnW = (cardW - 130) / 5 - 4;
  let vaporBtnH = 28;

  for (let i = 0; i < 5; i++) {
    let bx = vaporStartX + i * (vaporBtnW + 4);
    if (mouseX >= bx && mouseX <= bx + vaporBtnW && mouseY >= vaporY && mouseY <= vaporY + vaporBtnH) {
      selectedVapor = i;
      return;
    }
  }
}

function doSubmit() {
  if (submitted) return;
  if (selectedBeast < 0 || selectedVapor < 0) return;

  let tech = technologies[currentTech];
  let beastCorrect = beasts[selectedBeast].name === tech.correctBeast;
  let vaporCorrect = selectedVapor === tech.correctVapor;

  if (beastCorrect && vaporCorrect) {
    scores[currentTech] = 2;
  } else if (beastCorrect || vaporCorrect) {
    scores[currentTech] = 1;
  } else {
    scores[currentTech] = 0;
  }

  submitted = true;
}

function doNext() {
  currentTech = (currentTech + 1) % technologies.length;
  selectedBeast = -1;
  selectedVapor = -1;
  submitted = false;
  beastSelect.value('-1');
}

function doReset() {
  currentTech = 0;
  selectedBeast = -1;
  selectedVapor = -1;
  submitted = false;
  scores = new Array(technologies.length).fill(-1);
  beastSelect.value('-1');
}

function touchStarted() {
  mousePressed();
  return false;
}
